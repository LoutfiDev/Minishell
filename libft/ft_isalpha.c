#include "libft.h"

int	ft_isalpha(int c)
{
	unsigned char	var;

	var = (unsigned char)c;
	if (((var >= 65 && var <= 90) || (var >= 97 && var <= 122)) && c < 256)
		return (1);
	return (0);
}
